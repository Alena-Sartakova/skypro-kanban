<script setup>
import BaseHeader from '@/components/BaseHeader.vue'
import TaskDesk from '@/components/TaskDesk.vue'
import { fetchTasks } from '@/servises/api'
import { inject, onMounted, onUnmounted, provide, ref, watch } from 'vue'

const tasks = ref([])
const loading = ref(false)
const error = ref('')
const isOffline = ref(false)
const { userInfo } = inject('auth')

// –î–æ–±–∞–≤–ª—è–µ–º –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–∏ —Å–µ—Ç–µ–≤–æ–≥–æ —Å—Ç–∞—Ç—É—Å–∞
const updateNetworkStatus = () => {
  isOffline.value = !navigator.onLine
  if (!isOffline.value && error.value.includes('–∏–Ω—Ç–µ—Ä–Ω–µ—Ç')) {
    error.value = ''
  }
}

onMounted(() => {
  window.addEventListener('online', updateNetworkStatus)
  window.addEventListener('offline', updateNetworkStatus)
  updateNetworkStatus()
})

onUnmounted(() => {
  window.removeEventListener('online', updateNetworkStatus)
  window.removeEventListener('offline', updateNetworkStatus)
})

provide('tasksData', { tasks, loading, error, isOffline })

const getTasks = async () => {
  if (!userInfo.value?.token) {
    error.value = '–û—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç —Ç–æ–∫–µ–Ω –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏–∏'
    return
  }

  if (isOffline.value) {
    error.value = '–û—à–∏–±–∫–∞: —É—Å—Ç—Ä–æ–π—Å—Ç–≤–æ –Ω–µ –ø–æ–¥–∫–ª—é—á–µ–Ω–æ –∫ –∏–Ω—Ç–µ—Ä–Ω–µ—Ç—É'
    return
  }

  try {
    loading.value = true
    error.value = ''

    const data = await fetchTasks({
      token: userInfo.value.token,
    })

    if (data) tasks.value = data
  } catch (err) {
    handleFetchError(err)
  } finally {
    loading.value = false
  }
}

const handleFetchError = (err) => {
  if (err.name === 'OfflineError' || !navigator.onLine) {
    error.value = '–ù–µ—Ç –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è –∫ –∏–Ω—Ç–µ—Ä–Ω–µ—Ç—É. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ —Å–µ—Ç—å.'
    isOffline.value = true
  } else if (err.response?.status === 401) {
    error.value = '–°–µ—Å—Å–∏—è –∏—Å—Ç–µ–∫–ª–∞. –¢—Ä–µ–±—É–µ—Ç—Å—è –ø–æ–≤—Ç–æ—Ä–Ω–∞—è –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏—è.'
  } else {
    error.value = err.message || '–û—à–∏–±–∫–∞ –ø—Ä–∏ –∑–∞–≥—Ä—É–∑–∫–µ –∑–∞–¥–∞—á'
  }

  console.error('–û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–æ–ª—É—á–µ–Ω–∏–∏ –∑–∞–¥–∞—á:', err)
}

const retryFetch = () => {
  isOffline.value = false
  error.value = ''
  getTasks()
}

watch(userInfo, getTasks, { immediate: true })
</script>

